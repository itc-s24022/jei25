public class D49Q20kadai {
    public static void main(String[] args) {
        double[][] a = new double[5][10];

        for (int i = 0; i < a.length; i++) {
            for (int j = 0; j < a[i].length; j++) {
                System.out.print(a[i][j] + " ");
            }
            System.out.println();
        }
    }
}///
